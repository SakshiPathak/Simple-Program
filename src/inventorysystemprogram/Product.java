    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
              String productid=txtproductid.getText();
               String productname=txtproductname.getText();
               String quantity=txtquantity.getText();
               String price=txtprice.getText();
        
        if(productid.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Product id field is empty...please fill it first");
        }
        else if(productname.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Product name field is empty...please fill it first");
        }
        else if(quantity.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Quantity field is empty...please fill it first");
        }
        else if(price.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Price field is empty...please fill it first");
        }
        else if(!(Pattern.matches("^[0-9]+", txtproductid.getText())))
        {
            JOptionPane.showMessageDialog(this, "Please enter valid product id");
        }
        else if(!(Pattern.matches("^[a-zA-Z ,]+$", txtproductname.getText())))
        {
            JOptionPane.showMessageDialog(this, "Please enter valid name");
        }
        else if(!(Pattern.matches("^[0-9]+", txtquantity.getText())))
        {
            JOptionPane.showMessageDialog(this, "Please enter Quantity in digits only");
        }
         else if(!(Pattern.matches("^[0-9]+", txtprice.getText())))
        {
            JOptionPane.showMessageDialog(this, "Please enter Price in digits only");
        }
        else
        {
        try
        {
            con = DBConnection.getConnection();
            pst=con.prepareStatement("insert into productdetails values(?,?,?,?)");
            pst.setString(1, productid);
            pst.setString(2, productname);
            pst.setString(3, quantity);
            pst.setString(4, price);
            int i=pst.executeUpdate();
            if(i>0)
            {
               JOptionPane.showMessageDialog(this, "data added successfully");
               clear();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "data not added successfully");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_btnaddActionPerformed
    }
