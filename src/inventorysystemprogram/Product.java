    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        String productid=txtproductid.getText();
        try
        {
            //Class.forName("com.mysql.jdbc.Driver");
            //con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorysystem","root","sakshi");
            con = DBConnection.getConnection();
            pst=con.prepareStatement("select * from productdetails where productid=?");
            pst.setString(1, productid);
            rs=pst.executeQuery();
            if(productid.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Product id field is empty...please fill it first");
            }
            else if(!(Pattern.matches("^[0-9]+", txtproductid.getText())))
            {
                JOptionPane.showMessageDialog(this, "Please enter valid product id");
            }
            if(rs.next())
            {
                txtproductid.setText(rs.getString("productid"));
                txtproductname.setText(rs.getString("productname"));
                txtquantity.setText(rs.getString("quantity"));
                txtprice.setText(rs.getString("price"));
               
            }
            else
            {
                JOptionPane.showMessageDialog(this, "not searched");
                txtproductid.setText("");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnsearchActionPerformed

